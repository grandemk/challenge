def tab_state(tab):
    s = ""
    for i, val in enumerate(tab):
        if (i%8==0 and i != 0):
            s = s + "\n"
        s = "{} {:02x}".format(s, val)
    print(s)

def xor_tab(tab):
    c  = 0x0
    while (c <= 0x32):
        eax = tab[c]
        tab[c] = eax ^ 0x75
        c += 1

def tab_state_before(tab):
    print("before")
    tab_state(tab)

def tab_state_after(tab):
    print("after")
    tab_state(tab)

